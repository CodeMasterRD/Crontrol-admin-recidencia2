
package Model.EstudianteModel;

public class EstudianteModel {
    
    
    public static String nombre;
    public static String apellido;
    public static int matricula;
    public static String estado;

    public static String getEstado() {
        return estado;
    }

    public static void setEstado(String estado) {
        EstudianteModel.estado = estado;
    }

    public static String getApellido() {
        return apellido;
    }

    public static void setApellido(String apellido) {
        EstudianteModel.apellido = apellido;
    }
    
    
    public static int getMatricula() {
        return matricula;
    }

    public static void setMatricula(int matricula) {
        EstudianteModel.matricula = matricula;
    }
    

    
    public static String getNombre() {
        return nombre;
    }

    public static void setNombre(String nombre) {
        EstudianteModel.nombre = nombre;
    }

     
     
     
     
     
     
}
